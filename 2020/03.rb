PLAYFIELD = DATA.read.split("\n").freeze

class Ride
  attr_accessor :x_pos, :y_pos, :sequence_length, :sequence_width, :tree_hit_counter, :right, :down, :playfield

  def initialize(right, down)
    self.playfield = PLAYFIELD.dup
    self.sequence_length = playfield.size - 1 # empty new line
    self.sequence_width = playfield.first.size
    self.x_pos = 0
    self.y_pos = 0
    self.tree_hit_counter = 0
    self.right = right
    self.down = down
  end

  def start_ride
    while y_pos < sequence_length do
      move_slide
      self.tree_hit_counter +=1 if hit_tree?
    end

    pp "Hit #{tree_hit_counter} trees on #{right} right, #{down} down path.. OUCH"
  end

  def move_slide
    self.y_pos += down
    self.x_pos += right
    # self.x_pos = x_pos - sequence_width if x_pos >= sequence_width
  end

  def hit_tree?
    playfield[y_pos][x_pos] == '#'
  end
end

ride_results = [[1,1], [3, 1], [5, 1], [7, 1], [1, 2]].map do |directions|
  ride = Ride.new(directions[0], directions[1])
  ride.start_ride

  ride.tree_hit_counter
end

pp ride_results.reduce(:*)

